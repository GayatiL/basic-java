class narrowlong
{
	public static void main(String[] args) {
		 


		 long a = 1234567;

		 int i =(int)a;  //1234567

		 short s =(short)a;    //-10617

		 byte b =(byte)a;      //-121

		 char c = (char)a;     //?

		
		 System.out.println(i);
		 System.out.println(s);
		 System.out.println(b);
		 System.out.println(c);    //

	}
}