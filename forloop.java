class forloop
{
	public static void main(String[] args) {
		

		for(int i = 1; i <=10; i++)
		{
			System.out.print(i  + " _");
		}

        System.out.println();

		for(char a='A'; a <='Z'; a++)
		{
			System.out.print(a+" _");
		}
         System.out.println();


		for(char c ='z'; c>='a';c--)
		{
			System.out.print(c+"_");
		}

		System.out.println();

		for(char c ='0'; c<='9';c++)
		{
			System.out.print(c+"_");
		}

		for(int i=1; c<=127;c--)
		{
			System.out.print(c+"_");
		}




	}
}