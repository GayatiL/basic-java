class narrowdouble
{
	public static void main(String[] args) {
		 
		 double a = 120.35678;

		 float f =(float)a;

		 long l = (long)a;

		 int i =(int)a;

		 short s =(short)a;

		 byte b =(byte)a;

		 System.out.println(f);
		 System.out.println(l);
		 System.out.println(i);
		 System.out.println(s);
		 System.out.println(b);

	}
}